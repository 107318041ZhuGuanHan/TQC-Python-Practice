a = int(input('請輸入第一個整數: '))
b = int(input('請輸入第二個整數: '))
c = int(input('請輸入第三個整數: '))
d = int(input('請輸入第四個整數: '))

print('|%5d %5d|' % (a, b))
print('|%5d %5d|' % (c, d))
print('|%-5d %-5d|' % (a, b))
print('|%-5d %-5d|' % (c, d))

# %5d -> 那個字元站5個空格 -> 數字給定多少就佔幾個空格
#
# %5d -> 正的(前面沒負號) -> 靠右對齊(那5個空格的右邊)
#
# %-5d -> 負的(前面有負號) -> 靠左對齊(那5個空格的左邊)
#
# %d -> 輸出整數


