a = float(input('請輸入第一個浮點數: '))
b = float(input('請輸入第二個浮點數: '))
c = float(input('請輸入第三個浮點數: '))
d = float(input('請輸入第四個浮點數: '))

print("|%7.2f %7.2f|" % (a, b))
print("|%7.2f %7.2f|" % (c, d))
print("|%-7.2f %-7.2f|" % (a, b))
print("|%-7.2f %-7.2f|" % (c, d))

# %7.2f -> 欄寬為7，輸出浮點數到小數點第2位，靠右對齊 (靠7格欄寬的右邊)
#
# %-7.2f -> 欄寬為7，輸出浮點數到小數點第2位，靠左對齊 (靠7格欄寬的左邊)
#
# %f -> 輸出浮點數

